package org.pin.entity;

public class Attribution {
	
	private String title;
	private String url;
	private String authorUrl;
	private String authorName;
	private String providerName;
	private String providerFaviconUrl;
	
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the authorUrl
	 */
	public String getAuthorUrl() {
		return authorUrl;
	}
	/**
	 * @param authorUrl the authorUrl to set
	 */
	public void setAuthorUrl(String authorUrl) {
		this.authorUrl = authorUrl;
	}
	/**
	 * @return the authorName
	 */
	public String getAuthorName() {
		return authorName;
	}
	/**
	 * @param authorName the authorName to set
	 */
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	/**
	 * @return the providerName
	 */
	public String getProviderName() {
		return providerName;
	}
	/**
	 * @param providerName the providerName to set
	 */
	public void setProviderName(String providerName) {
		this.providerName = providerName;
	}
	/**
	 * @return the providerFaviconUrl
	 */
	public String getProviderFaviconUrl() {
		return providerFaviconUrl;
	}
	/**
	 * @param providerFaviconUrl the providerFaviconUrl to set
	 */
	public void setProviderFaviconUrl(String providerFaviconUrl) {
		this.providerFaviconUrl = providerFaviconUrl;
	}
}
